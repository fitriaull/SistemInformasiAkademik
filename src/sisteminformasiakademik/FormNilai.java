    private void tampilComboNIM() {
        try {
            Connection conn = Koneksi.getKoneksi();
            ResultSet res = conn.createStatement().executeQuery("SELECT nim FROM mahasiswa");
            cmbNIM.removeAllItems();
            while(res.next()) {
                cmbNIM.addItem(res.getString("nim"));
            }
        } catch (Exception e) {
            System.out.println("Error NIM: " + e.getMessage());
        }
} 
    private void hitungIPS() {
        try {
            double totalPoin = 0;
            int totalSKS = 0;
            for (int i = 0; i < tblNilai.getRowCount(); i++) {
                if (tblNilai.getValueAt(i, 2) != null && tblNilai.getValueAt(i, 3) != null) {
                    String nilaiHuruf = tblNilai.getValueAt(i, 3).toString().trim().toUpperCase();
                    if (nilaiHuruf.isEmpty()) continue;
                    
                    int sks = Integer.parseInt(tblNilai.getValueAt(i, 2).toString());
                    int bobot = (nilaiHuruf.equals("A"))?4:(nilaiHuruf.equals("B"))?3:(nilaiHuruf.equals("C"))?2:(nilaiHuruf.equals("D"))?1:0;
                    
                    totalPoin += (sks * bobot);
                    totalSKS += sks;
                }
            }
            if (totalSKS > 0) lblIPS.setText(String.format("%.2f", totalPoin / totalSKS));
        } catch (Exception e) {
            System.out.println("Error Hitung: " + e.getMessage());
        }
    }
