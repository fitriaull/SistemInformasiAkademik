    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
        Connection conn = Koneksi.getKoneksi();
        for (int i = 0; i < tblNilai.getRowCount(); i++) {
            String kodeMK = tblNilai.getValueAt(i, 0).toString();
            String huruf = tblNilai.getValueAt(i, 3).toString();
            
            int bobot = 0;
    if (huruf.equalsIgnoreCase("A")) bobot = 4;
    else if (huruf.equalsIgnoreCase("B")) bobot = 3;
    else if (huruf.equalsIgnoreCase("C")) bobot = 2;
    else if (huruf.equalsIgnoreCase("D")) bobot = 1;
    else bobot = 0;

            String sql = "INSERT INTO nilai (nim, kode_mk, semester, nilai_huruf, bobot) VALUES (?,?,?,?,?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, cmbNIM.getSelectedItem().toString());
            pst.setString(2, kodeMK);
            pst.setString(3, cmbSemester.getSelectedItem().toString());
            pst.setString(4, huruf);
            pst.setInt(5, bobot);
            pst.executeUpdate();
        }
        JOptionPane.showMessageDialog(null, "Nilai Berhasil Disimpan!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void cmbNIMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbNIMActionPerformed
        // TODO add your handling code here:
        try {
            DefaultTableModel model = (DefaultTableModel) tblNilai.getModel();
            model.setRowCount(0); 
            
            Connection conn = Koneksi.getKoneksi();
            ResultSet rsMhs = conn.createStatement().executeQuery("SELECT nama FROM mahasiswa WHERE nim='" + cmbNIM.getSelectedItem() + "'");
            if(rsMhs.next()) txtNama.setText(rsMhs.getString("nama"));

            String sql = "SELECT krs.kode_mk, matakuliah.nama_mk, matakuliah.sks FROM krs " +
             "INNER JOIN matakuliah ON krs.kode_mk = matakuliah.kode_mk " +
             "WHERE krs.nim='" + cmbNIM.getSelectedItem() + "' " +
             "AND krs.semester='" + cmbSemester.getSelectedItem() + "'";
            
            ResultSet rsKrs = conn.createStatement().executeQuery(sql);
            while(rsKrs.next()) {
                model.addRow(new Object[]{rsKrs.getString(1), rsKrs.getString(2), rsKrs.getString(3), ""});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    
    }//GEN-LAST:event_cmbNIMActionPerformed

